package com.mycompany.a1;



public abstract class Fixed extends GameObject{
	
	/**
	 * Instantiates a new fixed.
	 *
	 * @param x the x
	 * @param y the y
	 * @param s the s
	 * @param r the r
	 * @param g the g
	 * @param b the b
	 */
	public Fixed(float x, float y, int s, int r, int g, int b) {
		super(x, y, s, r, g, b);
	}
	
	
	
	
	
	
	/**
	 * To string.
	 *
	 * @return the string
	 */
	public String toString() {
		return super.toString();
	}
	
}
